  void validateLogin() {
    String _email = loginEmailController.text.trim();
    String _pass = loginPasswordController.text.trim();
    if (
    _email.length > 0 &&
        _pass.length > 0) {
      RegExp regex = new RegExp(Constants.emailPattern);
      if (regex.hasMatch(_email)) {
        doLogin(_email, _pass);
      } else {
        print("Email is invalid!");
        Toast.show("Email is invalid!", context,
            duration: Toast.LENGTH_SHORT,
            gravity: Toast.CENTER,
            textColor: Colors.red);
      }
    } else {
      print("Some fields are empty!");
      Toast.show("Some fields are empty!", context,
          duration: Toast.LENGTH_SHORT,
          gravity: Toast.CENTER,
          textColor: Colors.red);
    }
  }

  void validateSignUp() {
    String _name = signupNameController.text;
    String _email = signupEmailController.text.trim();
    String _pass1 = signupPasswordController.text.trim();
    String _pass2 = signupConfirmPasswordController.text.trim();
    if (_name.length > 0 &&
        _email.length > 0 &&
        _pass1.length > 0 &&
        _pass2.length > 0) {
      RegExp regex = new RegExp(Constants.emailPattern);
      if (regex.hasMatch(_email)) {
        if (_pass1 == _pass2) {
          doRegister(_name, _email, _pass1);
        } else {
          print("Passwords must match!");
          Toast.show("Passwords must match!", context,
              duration: Toast.LENGTH_SHORT,
              gravity: Toast.CENTER,
              textColor: Colors.red);
        }
      } else {
        print("Email is invalid!");
        Toast.show("Email is invalid!", context,
            duration: Toast.LENGTH_SHORT,
            gravity: Toast.CENTER,
            textColor: Colors.red);
      }
    } else {
      print("Some fields are empty!");
      Toast.show("Some fields are empty!", context,
          duration: Toast.LENGTH_SHORT,
          gravity: Toast.CENTER,
          textColor: Colors.red);
    }
  }

  void doRegister(String _name, String _email, String _pass1) async {
    //countDocuments(_name, _email, _pass1);
    QuerySnapshot _myDoc = await Firestore.instance
        .collection('profile_data')
        .where("email", isEqualTo: _email)
        .getDocuments();
    List<DocumentSnapshot> _myDocCount = _myDoc.documents;
    print(_myDocCount.length); // Count of Documents in Collection
    if (_myDocCount.length == 0) {
      Firestore.instance
          .collection('profile_data')
          .add({'name': _name, 'email': _email, 'password': _pass1}).then(
              (erg) {
            print("Success!");
            signupNameController.clear();
            signupEmailController.clear();
            signupPasswordController.clear();
            signupConfirmPasswordController.clear();
            Toast.show("Registration Succesfull!", context,
                duration: Toast.LENGTH_SHORT, gravity: Toast.CENTER);
          }, onError: (e) {
        print("Fail!");
        Toast.show("Registration Error!", context,
            duration: Toast.LENGTH_SHORT, gravity: Toast.CENTER);
      });
    } else {
      Toast.show("Email already registered!", context,
          duration: Toast.LENGTH_SHORT, gravity: Toast.CENTER);
    }
  }


  void doLogin(String _email, String _pass1) async {
    QuerySnapshot _myDoc = await Firestore.instance
        .collection('profile_data')
        .where("email", isEqualTo: _email)
        .where("password", isEqualTo: _pass1)
        .getDocuments();
    List<DocumentSnapshot> _myDocCount = _myDoc.documents;
    print(_myDocCount.length); // Count of Documents in Collection
    if (_myDocCount.length == 0) {
      print("Fail!");
      Toast.show("Login Error!", context,
          duration: Toast.LENGTH_SHORT, gravity: Toast.CENTER);
    } else {
      DocumentSnapshot doc = _myDocCount.elementAt(0);
      print(doc.documentID);
      print("Success!");

      SharedPreferences prefs = await SharedPreferences.getInstance();
      prefs.setString("id", doc.documentID);
      prefs.setString("name",doc.data["name"]);
      prefs.setString("email",doc.data["email"]);
      prefs.setBool("login", true);
      Toast.show("Login Success!", context,
          duration: Toast.LENGTH_SHORT, gravity: Toast.CENTER);
      Navigator.of(context).pushNamedAndRemoveUntil(
          "home", (Route<dynamic> route) => false);
    }
  }

